function generateNickname(adjectives, nouns) {
  if (
    Array.isArray(adjectives) &&
    Array.isArray(nouns) &&
    adjectives.length > 0 &&
    nouns.length > 0
  ) {
    const randomAdjectiveIndex = Math.floor(Math.random() * adjectives.length)
    const randomNounIndex = Math.floor(Math.random() * nouns.length)
    const firstName = adjectives[randomAdjectiveIndex]
    const lastName = nouns[randomNounIndex]
    return `${firstName} ${lastName}`
  }
  return 'Chrisp'
}

function generateEmail() {
  const randomString = Math.random().toString(36).substr(2, 8)
  return `sandbox.${randomString}@chrisp.chat`
}

function generatePhone() {
  return '+33' + Math.floor(100000000 + Math.random() * 900000000)
}

function generateCompany(companies) {
  if (Array.isArray(companies) && companies.length > 0) {
    const randomCompanyIndex = Math.floor(Math.random() * companies.length)
    const company = companies[randomCompanyIndex]
    return company
  }
  return 'La Fistinière'
}

function generateAvatar(avatars) {
  if (Array.isArray(avatars) && avatars.length > 0) {
    const randomAvatarIndex = Math.floor(Math.random() * avatars.length)
    const avatar = avatars[randomAvatarIndex]
    return avatar
  }
  return 'https://crisp-sandbox.netlify.app/images/avatars/mcfly.png'
}

function generateData(datas, values) {
  if (Array.isArray(datas) && Array.isArray(values) && datas.length > 0 && values.length > 0) {
    const randomDataIndex = Math.floor(Math.random() * datas.length)
    const randomValueIndex = Math.floor(Math.random() * values.length)
    const dataKey = datas[randomDataIndex]
    const dataValue = values[randomValueIndex]
    return { dataKey, dataValue }
  }
  return { dataKey: 'defaultKey', dataValue: 'defaultValue' }
}

function generateSegment(segments) {
  if (Array.isArray(segments) && segments.length > 0) {
    const randomSegmentIndex = Math.floor(Math.random() * segments.length)
    const segment = segments[randomSegmentIndex]
    return segment
  }
  return 'segment'
}

function generateEvent() {
  return 'event:test'
}

function generateTrigger() {
  return 'trigger-test'
}

function generateSendMessage(sendMessage) {
  if (typeof sendMessage === 'string') {
    return sendMessage
  }
  return 'Hello world'
}

function generateShowMessage(showMessage) {
  if (typeof showMessage === 'string') {
    return showMessage
  }
  return 'Hello world'
}

export default function useMethodGenerator(id) {
  const generators = {
    'session-nickname': () => generateNickname(options.adjectives, options.nouns),
    'session-email': generateEmail,
    'session-phone': generatePhone,
    'session-company': () => generateCompany(options.companies),
    'session-avatar': generateAvatar,
    'session-data': () => generateData(options.datas, options.values),
    'session-segment': () => generateSegment(options.segments),
    event: generateEvent,
    trigger: generateTrigger,
    'send-message': () => generateSendMessage(options.sendMessage),
    'show-message': () => generateShowMessage(options.showMessage)
  }

  return generators[id] ? generators[id]() : null
}
